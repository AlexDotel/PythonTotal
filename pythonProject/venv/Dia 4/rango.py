# RANGO

# Forma ambigua de contar hasta el 5:
lista = [1,2,3,4,5]

for numero in lista:
    print(numero)
print("\n")

# Con rango:
for numero in range(5):
    print(numero)
print("\n")
# Contando de uno en uno
print("\n")

for numero in range(10,20):
    print(numero)
# Contando de un numero x a un numero y
print("\n")

for numero in range(10,20,2):
    print(numero)
# Contando de un numero x a un numero y de dos en dos

lista = list(range(1,100))
print(lista)
# Tambien se puede castear un rango dentro de una lista.